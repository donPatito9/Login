    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        // TODO add your handling code here:
        String usuario = txtUsuario.getText();
        String contrasenia = txtPassword.getText();
        String rol = (String) cmbRol.getSelectedItem();

        //pasar parametro usu variable global para usar el usuario oroginal
        controlLogic.editarUsuario(usu, usuario, contrasenia, rol);

        mostrarMensaje("Usuario editado correctamente", "Info", "Edici칩n exitosa");
        //al guardar los cambios cerramos la ventana edicion de usuarios
        this.dispose();
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        //buscar el usuario
        usu = controlLogic.traerUsuario(id_usuario);
        
        //cargar datos en interfaz
        txtUsuario.setText(usu.getNombreUsuario());
        txtPassword.setText(usu.getContrasenia());
        
        //cargar roles de forma generica
       List <Rol> listadoRoles = controlLogic.traerRoles();
        
        if (listadoRoles != null){    
        
        for (Rol rol : listadoRoles){
            cmbRol.addItem(rol.getNombreRol());
           }
        }           
        //seleccionar rol de el usuario en cuesti칩n
        String rol = usu.getUnRol().getNombreRol();
        
        int cantidadItems = cmbRol.getItemCount();
         //recorrer el combo
        for (int i = 0; i < cantidadItems; i++) {
            
            if (String.valueOf(cmbRol.getItemAt(i)).equals(rol)) {
                cmbRol.setSelectedIndex(i);
            
        }
        
      }
    }//GEN-LAST:event_formWindowOpened

     public void mostrarMensaje(String mensaje, String tipo, String titulo){
     JOptionPane optionPane = new JOptionPane(mensaje);
     if (tipo.equals("Info")){
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
     }
     else if(tipo.equals("Error")){
          optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
     }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
     
     }
  

